     * @return void
     */
    public function __construct($details)
    {
        $this->details = $details;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
       $from_name = "SunFlower";
       $from_email = "thanhelma2020@gmail.com";
       $subject = "Send Mail accept your Order";
       return $this->from($from_email, $from_name)
                    ->view('pages.sendmail')
    }
}
